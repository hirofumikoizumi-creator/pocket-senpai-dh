/**
 * キーワードマッチングで回答を検索する関数
 * 将来的にOpenAI APIに差し替え可能
 */
export function findConsultationResponse(query: string): ConsultationResponse | null {
  const normalizedQuery = query.toLowerCase();
  
  let bestMatch: ConsultationResponse | null = null;
  let bestScore = 0;

  for (const item of consultationData) {
    let score = 0;
    for (const keyword of item.keywords) {
      if (normalizedQuery.includes(keyword.toLowerCase())) {
        score += 1;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = item;
    }
  }

  // スコアが0の場合はデフォルト回答を返す
  if (bestScore === 0) {
    return {
      id: 'default',
      category: '一般',
      keywords: [],
      conclusion: 'ご相談ありがとうございます。具体的な状況をもう少し教えていただけると、より的確なアドバイスができます。',
      fieldAction: 'まずは落ち着いて状況を整理しましょう。①何が起きているか ②いつから困っているか ③誰に相談できるか、を考えてみてください。',
      patientTalk: '',
      caution: '判断に迷う場合は、必ず歯科医師や先輩に確認しましょう。一人で抱え込まないことが大切です。',
      senpaiMessage: '悩んでいる時点で、あなたは真剣に仕事に向き合っている証拠だよ。一緒に考えていこうね！',
    };
  }

  return bestMatch;
}
